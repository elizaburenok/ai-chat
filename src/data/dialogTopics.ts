/**
 * Static scripted dialog content per skill. No LLM — playback in order.
 * Keyed by SkillItem.id (see homeSkills).
 */

export interface DialogTopic {
  topic: string;
  clientName: string;
  questions: string[];
  farewellMessage: string;
}

const placeholderFollowUp = 'А можете чуть подробнее объяснить?';

function topic(
  skillTitle: string,
  clientName: string,
  firstQuestion: string,
  farewellMessage: string
): DialogTopic {
  return {
    topic: skillTitle,
    clientName,
    questions: [firstQuestion, placeholderFollowUp],
    farewellMessage,
  };
}

